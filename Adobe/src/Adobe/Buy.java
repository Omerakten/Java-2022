package Adobe;

public class Buy {

	private String creditCardOwnerName;
	private String creditCardNumber;
	private String creditCardSecurityNumber;
	private String creditCardDate;

	
	public String getCreditCardOwnerName() {
		return creditCardOwnerName;
	}

	public void setCreditCardOwnerName(String creditCardOwnerName) {
		this.creditCardOwnerName = creditCardOwnerName;
	}

	public String getCreditCardNumber() {
		return creditCardNumber;
	}

	public void setCreditCardNumber(String creditCardNumber) {
		this.creditCardNumber = creditCardNumber;
	}

	public String getCreditCardSecurityNumber() {
		return creditCardSecurityNumber;
	}

	public void setCreditCardSecurityNumber(String creditCardSecurityNumber) {
		this.creditCardSecurityNumber = creditCardSecurityNumber;
	}

	public String getCreditCardDate() {
		return creditCardDate;
	}

	public void setCreditCardDate(String creditCardDate) {
		this.creditCardDate = creditCardDate;
	}

}
