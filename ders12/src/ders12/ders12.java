package ders12;

public class ders12 {

	public static void main(String[] args) {
		//for
		for (int i = 2; i < 10; i+=2) {
			
			System.out.println(i);
			
		}
		System.out.println("döngü bitti");
	
	// while
	int i=2;
	while(i<10) {
		i+=2;
		System.out.println(i);
		
	}
	System.out.println("While döngüsü bitti");
	
	
	
	
	
	}
}
